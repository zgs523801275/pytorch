#denselayer模块：
#每个denselayer的输出特征维度均为32，即采用k=32
#每个denselayer的输入和输出作为下一级denselayer的输入，以此可以减轻梯度消失现象
#每个denselayer中的1*1卷积既减少特征维度和参数数量，同时也解决了特征合并时的size问题
#transition模块：
#均采用1个1*1卷积组成，主要用于降维
